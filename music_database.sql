-- Drop the database if it exists
drop database if exists MUSIC;

-- Create music database
create database MUSIC;

-- Use the MUSIC database
use MUSIC;

-- Create table for LABELS
create table LABELS(
  Label_ID int not null AUTO_INCREMENT,
  Label_Name varchar(50),
  Owner_FName varchar(50),
  Owner_LName varchar(50),
  State_Location varchar(50),
  Date_Established date,
  Phone_Number varchar(10),
  Email varchar(75),
  primary key(Label_ID)
);
-- Create table for Artists
create table ARTISTS(
  Artist_ID int not null AUTO_INCREMENT,
  Artist_Name varchar(50),
  F_Name varchar(50),
  L_Name varchar(50),
  Date_Started date,
  Label_ID int not null,
  primary key(Artist_ID),
  FOREIGN KEY (Label_ID) REFERENCES LABELS(Label_ID)
);

-- Create table for MANAGERS 
create table MANAGERS(
  Manager_ID int not null AUTO_INCREMENT,
  F_Name varchar(50),
  L_Name varchar(50),
  Date_Started date,
  Phone_number varchar(12),
  Email varchar(75),
  Artist_ID int not null,
  primary key(Manager_ID),
  FOREIGN KEY (Artist_ID) REFERENCES ARTISTS(Artist_ID)
);

-- Create table for ALBUMS 
create table ALBUMS (
  Album_ID int not null AUTO_INCREMENT,
  Album_Name varchar(50),
  Release_Date date,
  Genre varchar(20),
  Duration time,
  Artist_ID int not null,
  Label_ID int not null,
  primary key(Album_ID),
  FOREIGN KEY (Artist_ID) REFERENCES ARTISTS(Artist_ID),
  FOREIGN KEY (Label_ID) REFERENCES LABELS(Label_ID)
);

-- Create table for SALES 
create table SALES (
  Sales_ID int not null AUTO_INCREMENT,
  Units_Sold int,
  Digital_streams int,
  Total_Units int,
  RIAA_Certification varchar(25),
  Last_Updated date,
  Album_ID int not null,
  primary key(Sales_ID),
  FOREIGN KEY (Album_ID) REFERENCES ALBUMS(Album_ID)
);

